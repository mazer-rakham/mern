const mongoose = require("mongoose");
const Schema = mongoose.Schema;

// Create the Schema for the Profile
const ProfileSchema = new Schema({
  // set the user object to the Schema tyoe and grab the ID from the collection users
  user: {
    type: Schema.Types.ObjectId,
    ref: "users"
  },
  // setting up the url handle for the users
  handle: {
    type: String,
    required: true,
    max: 40
  },
  company: {
    type: String
  },
  website: {
    type: String
  },
  location: {
    type: String
  },
  // this is actually a select box but the box outputs a string that we will use here
  status: {
    type: String,
    required: true
  },
  skills: {
    // mongoose would like an array of strings
    type: [String],
    required: true
  },
  bio: {
    typw: String
  },
  githubusername: {
    type: String
  },
  experience: [
    {
      title: {
        type: String,
        required: true
      },
      company: {
        type: String,
        required: true
      },
      location: {
        type: String
      },
      from: {
        type: Date,
        required: true
      },
      to: {
        type: Date
      },
      current: {
        type: Boolean,
        default: false
      },
      description: {
        type: String
      }
    }
  ],
  education: [
    {
      school: {
        type: String,
        required: true
      },
      degree: {
        type: String,
        required: true
      },
      fieldofstudy: {
        type: String,
        required: true
      },
      from: {
        type: Date,
        required: true
      },
      to: {
        type: Date
      },
      current: {
        type: Boolean,
        default: false
      },
      description: {
        type: String
      }
    }
  ],
  social: {
    youtube: {
      type: String
    },
    twitter: {
      type: String
    },
    facebook: {
      type: String
    },
    linkedin: {
      type: String
    },
    instagram: {
      type: String
    }
  },
  date: {
    type: Date,
    default: Date.now
  }
});

// export everything to the profile collection in mlab
module.exports = Profile = mongoose.model('profile', ProfileSchema);